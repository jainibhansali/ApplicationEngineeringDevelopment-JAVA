    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblcustomerDirectory.getModel();
        model.setRowCount(0);
        for(Customer c:enterprise.getCustomerDirectory().getCustomerList())
        {
          for(CustomerAccount ca: c.getCustomerAccountDirectory().getCustomerAccountList())
            {
        System.out.println("account:"+ca.getAccountType());
        Object[] row = new Object[3];
        row[0] = c;
        row[1] = c.getCustomerId();
        row[2]=ca.getAccountType();
        model.addRow(row);
        }
        
    }
    }

