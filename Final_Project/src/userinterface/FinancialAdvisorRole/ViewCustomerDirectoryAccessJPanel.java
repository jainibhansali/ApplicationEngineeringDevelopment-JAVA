    private void btnviewdetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewdetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow= tblcustomerDirectory.getSelectedRow();
        if(selectedRow<0)
        {
            JOptionPane.showMessageDialog(null,"Please select a row");
            return;
        }
        else
        {
            Customer c=(Customer)tblcustomerDirectory.getValueAt(selectedRow, 0);

            ViewCustomerDetailsbyFinancialAdvisorJPanel ViewCustomerDetailsbyFinancialAdvisorJPanel = new ViewCustomerDetailsbyFinancialAdvisorJPanel(userProcessContainer,enterprise,business,c,account);
            userProcessContainer.add("ViewCustomerDetailsbyFinancialAdvisorJPanel", ViewCustomerDetailsbyFinancialAdvisorJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        }
    }//GEN-LAST:event_btnviewdetailsActionPerformed

