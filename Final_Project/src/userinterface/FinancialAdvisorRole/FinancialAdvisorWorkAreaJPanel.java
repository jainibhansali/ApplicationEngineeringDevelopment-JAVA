    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        assignJButton.setEnabled(false);
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){

            JOptionPane.showMessageDialog(null,"Please select a row");
            return;
        }
        else
        {
        Create_Loan_Mortgage_Soc_WorkRequest request = (Create_Loan_Mortgage_Soc_WorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
         request.setStatus("Processing");
        if(request.getMessage().equalsIgnoreCase("Loan Request"))
        {
            
        System.out.println("Loan");
        

        ProcessLoanRequestJPanel ProcessLoanRequestJPanel = new ProcessLoanRequestJPanel(userProcessContainer, request,enterprise);
        userProcessContainer.add("ProcessLoanRequestJPanel", ProcessLoanRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
              layout.next(userProcessContainer);
        }
        else if(request.getMessage().equalsIgnoreCase("Mortgage Request"))
        {   
             System.out.println("Mortgage");
              ProcessMortgageRequestJPanel ProcessMortgageRequestJPanel = new ProcessMortgageRequestJPanel(userProcessContainer, request,enterprise);
            userProcessContainer.add("ProcessMortgageRequestJPanel", ProcessMortgageRequestJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
              layout.next(userProcessContainer);
              System.out.println("Status 2"+ request.getStatus());
             
            
        }
    }//GEN-LAST:event_processJButtonActionPerformed
    }
