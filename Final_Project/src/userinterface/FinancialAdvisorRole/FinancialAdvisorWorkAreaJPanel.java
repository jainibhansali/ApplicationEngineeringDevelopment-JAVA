    private void btnrequestAccessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrequestAccessActionPerformed
        // TODO add your handling code here:
        btnaccess.setEnabled(true);
        Create_Loan_Mortgage_Soc_WorkRequest request = new Create_Loan_Mortgage_Soc_WorkRequest();
        request.setSocMessage("Access Request");
        request.setMessage(txtmessage.getText().trim());
        request.setEmployeeSender(account);
        request.setStatus("Sent to manager");
        request.setEmployeeFlag(1);
        request.setManagerFlag(0);
        request.setSocFlag(0);
        request.setSender(account);
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof RetailOrganization){
               
                
                {
                org = organization;
                break;
                }
            }
        }
        if (org!=null){
            
            org.getWorkQueue().getWorkRequestList().add(request);
            account.getWorkQueue().getWorkRequestList().add(request);
        }
      
    }//GEN-LAST:event_btnrequestAccessActionPerformed

